const add = (a, b) => {
  const addTotal = a + b;
  return addTotal;
};

const subtract = (a, b) => {
  const subtractTotal = a - b;
  return subtractTotal;
};

const multiply = (a, b) => {
  const multiplyTotal = a * b;
  return multiplyTotal;
};

const divide = (a, b) => {
  const divideTotal = a / b;
  return divideTotal;
};
